clear    
N=200;    
w(1)=0;                     %w为过程噪声  
w=randn(1,N)    
x(1)=25;    
a=1;                        %a为方程中A(k)  
for k=2:N;    
x(k)=a*x(k-1)+w(k-1);    
end    
V=randn(1,N);               %V为观察噪声  
q1=std(V);    
Rvv=q1.^2;    
q2=std(x);    
Rxx=q2.^2;     
q3=std(w);    
Rww=q3.^2;    
c=0.2;                      %c为方程中H(k)  
Y=c*x+V;                    %Y为观察值  
p(1)=0;    
s(1)=0;    
for t=2:N;    
p1(t)=a.^2*p(t-1)+Rww;     %p1为方程中p'  
b(t)=c*p1(t)/(c.^2*p1(t)+Rvv);    
s(t)=a*s(t-1)+b(t)*(Y(t)-a*c*s(t-1));    
p(t)=p1(t)-c*b(t)*p1(t);    
end    
t=1:N;    
plot(t,s,'r',t,Y,'g',t,x,'b'); 